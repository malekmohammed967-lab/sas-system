import streamlit as st

def generate_triangle(n):
    base = (n - 12) // 3
    return [[base+7, base, base+5], [base+2, base+4, base+6], [base+3, base+8, base+1]]

st.set_page_config(page_title="نظام سي عبد الله الشامل", layout="wide")
st.title("🛡️ نظام سي عبد الله الروحاني - النسخة النهائية")

col1, col2 = st.columns(2)

with col1:
    name = st.text_input("اسم الطالب:")
    m_name = st.text_input("اسم الأم:")
    purpose = st.selectbox("مقصد العمل:", ["جلب", "فراق", "إبطال سحر", "رزق"])

with col2:
    date = st.date_input("تاريخ اليوم:")
    complaint = st.text_area("الشكوى/العارض:")

if st.button("🚀 تشغيل المحرك الروحاني"):
    # الحساب
    abjad = {'أ':1, 'ب':2, 'ج':3, 'د':4, 'ه':5, 'و':6, 'ز':7, 'ح':8, 'ط':9, 'ي':10, 'ك':20, 'ل':30, 'م':40, 'ن':50, 'س':60, 'ع':70, 'ف':80, 'ص':90, 'ق':100, 'ر':200, 'ش':300, 'ت':400, 'ث':500, 'خ':600, 'ذ':700, 'ض':800, 'ظ':900, 'غ':1000}
    total = sum(abjad.get(c, 0) for c in name + m_name)
    tabi = ["نار", "تراب", "هواء", "ماء"][total % 4]
    
    # المعطيات
    data = {
        "نار": {"ملك": "رقايائيل", "بخور": "لبان ذكر", "عزيمة": "يا معشر الأرواح النارية.."},
        "تراب": {"ملك": "كسفيائيل", "بخور": "مستكة", "عزيمة": "أقسمت عليكم يا خدام هذا الطبع.."},
        "هواء": {"ملك": "جبرائيل", "بخور": "عود", "عزيمة": "يا طاهر يا مطهر.."},
        "ماء": {"ملك": "عنقيائيل", "بخور": "جاوى", "عزيمة": "بسطوة الماء وطهارته.."}
    }
    
    # العرض
    st.success(f"النتيجة: {total} | الطبع: {tabi}")
    
    c1, c2 = st.columns(2)
    with c1:
        st.write(f"### الملوك والأبخرة:")
        st.write(f"- الملك الموكل: **{data[tabi]['ملك']}**")
        st.write(f"- البخور المطلوب: **{data[tabi]['بخور']}**")
        st.write(f"- العزيمة: _{data[tabi]['عزيمة']}_")
    
    with c2:
        st.write(f"### وفق المثلث (3x3):")
        grid = generate_triangle(total)
        for row in grid:
            st.code(f"{row[0]}    {row[1]}    {row[2]}")
            
    st.write("---")
    st.write("### 💡 استنتاج الذكاء الاصطناعي:")
    st.info(f"بناءً على {purpose}، النظام يرى أن الحالة تتطلب العمل وفق خصائص {tabi}. الطريقة هي الكتابة في ساعة {tabi} واستخدام البخور المحدد.")
